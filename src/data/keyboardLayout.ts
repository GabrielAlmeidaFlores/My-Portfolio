export const KEYBOARD_COLS = 18;
export const KEYBOARD_ROWS = 10;

export interface KeyboardKeyData {
  id: string;
  label: string;
}

export function generateKeyboardKeys(): KeyboardKeyData[] {
  const total = KEYBOARD_COLS * KEYBOARD_ROWS;

  return Array.from({ length: total }, (_, index) => ({
    id: `kb-${index}`,
    label: KEY_LABELS[index % KEY_LABELS.length] ?? "·",
  }));
}
